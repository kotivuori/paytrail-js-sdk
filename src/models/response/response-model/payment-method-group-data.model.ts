/**
 * Class PaymentMethodGroupData
 */
export class PaymentMethodGroupData {
  /**
   * ID of the group.
   */
  public id: string

  /**
   * Localized name of the group.
   */
  public name: string

  /**
   * URL to PNG version of the group icon.
   */
  public icon: string

  /**
   * URL to SVG version of the group icon. Using the SVG icon is preferred.
   */
  public svg: string
}
